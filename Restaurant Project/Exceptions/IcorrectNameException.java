package Exceptions;

public class IcorrectNameException extends Exception {

	private static final long serialVersionUID = 5876883448199128925L;

	public IcorrectNameException() {
		super();
	}

	public IcorrectNameException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IcorrectNameException(String arg0) {
		super(arg0);
	}

	public IcorrectNameException(Throwable arg0) {
		super(arg0);
	}

}
